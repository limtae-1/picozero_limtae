from picozero import OLED
from time import sleep

# OLED 디스플레이 설정 (SCL=5, SDA=4 핀 사용)
oled = OLED(scl=5, sda=4)
# SCL 핀 번호 5, SDA 핀 번호 4를 사용하여 OLED 객체를 생성합니다.

# 화면 지우기
oled.clear()
# OLED 화면을 지웁니다. 화면을 모두 검은색으로 채우고, 그 상태를 디스플레이에 반영합니다.

# 텍스트 출력
oled.text('Hello, World!', 0, 0)
# 좌표 (0, 0) 위치에 'Hello, World!' 텍스트를 출력합니다.

# 선 그리기
oled.line(0, 10, 127, 10, 1)
# 좌표 (0, 10)에서 (127, 10)까지 하얀색(1) 선을 그립니다.

# 사각형 그리기
oled.rect(0, 20, 50, 30, 1)
# 좌표 (0, 20) 위치에 너비 50, 높이 30의 하얀색(1) 사각형을 그립니다.

# 채워진 사각형 그리기
oled.fill_rect(60, 20, 50, 30, 1)
# 좌표 (60, 20) 위치에 너비 50, 높이 30의 채워진 하얀색(1) 사각형을 그립니다.

# 화면 반전
oled.invert(1)
# OLED 화면을 반전시킵니다. 즉, 검은색은 하얀색으로, 하얀색은 검은색으로 변경됩니다.

# 1초 대기
sleep(1)
# 1초 동안 대기합니다. 화면 반전 상태를 유지하는 시간입니다.

# 화면 복원
oled.invert(0)
# OLED 화면을 원래 상태로 복원합니다. 다시 한 번 색상이 반전됩니다.